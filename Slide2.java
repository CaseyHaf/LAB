public class Slide2 {
    public static void main(String args[]) {
      char c = 'A';
      int i = (int) c;
      System.out.println("A = " +i);
      
      float f = 1000.34F;
      int j = (int) f;
      System.out.println("1000.34F = " +j);
      
      double d = 1000.34;
      int k = (int) d;
      System.out.println("1000.34 = " +k);
      
      int l = 97;
      char n = (char) l;
      System.out.println("97 = " +n);
    }
}
